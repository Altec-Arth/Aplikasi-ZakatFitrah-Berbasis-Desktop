    public void getData(){
        Integer value;
        Integer min=0;
        Integer max=20;
        Integer step=1; 
        String sql="select * from dt_muzakki where nomor='"+datfarMuzakki.noEdit+"'";

        try{
            k.st=k.conn.createStatement();
            k.rs=k.st.executeQuery(sql);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Load data gagal");
            return;
        }
         try {
            k.rs.next();
            txtTanggal.setText(k.rs.getString("tglInput").toString());
            txtNomor.setText(Integer.toString(k.rs.getInt("nomor")));
            txtNamaMuzakki.setText(k.rs.getString("namaMuzakki").toString().toUpperCase());
            value=k.rs.getInt("jmlJiwa");
            modelJiwa=new SpinnerNumberModel(value,min,max,step);
            txtAlamat.setText(k.rs.getString("Alamat").toString().toUpperCase());
            spinnerJiwa.setModel(modelJiwa);
            txtZakatFitrah.setText(Integer.toString(k.rs.getInt("jmlZakat")));
            txtBeras.setText(Integer.toString(k.rs.getInt("jmlBeras")));
            txtPanitia.setText(koneksi.username);
            zakatUang=k.rs.getInt("jmlZakat");
            zakatBeras=k.rs.getInt("jmlBeras");
         } catch (SQLException e) {
             //JOptionPane.showMessageDialog(null,"Data gagal di perbarui");
         }
    }
    
