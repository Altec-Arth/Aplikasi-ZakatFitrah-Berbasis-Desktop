    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int jiwa=modelJiwa.getNumber().intValue();
        
        if(Integer.parseInt(txtZakatFitrah.getText())<zakatUang){
            zakatUang=zakatUang-Integer.parseInt(txtZakatFitrah.getText());
            zakatUang=datfarMuzakki.totalUang-zakatUang;
        }else if(Integer.parseInt(txtZakatFitrah.getText())>zakatUang){
            zakatUang=Integer.parseInt(txtZakatFitrah.getText())-zakatUang;
            zakatUang=datfarMuzakki.totalUang+zakatUang;
        }else{
            zakatUang=datfarMuzakki.totalUang;
        }
        if(Integer.parseInt(txtBeras.getText())<zakatBeras){
            zakatBeras=zakatBeras-Integer.parseInt(txtBeras.getText());
            zakatBeras=datfarMuzakki.totalBeras-zakatBeras;
        }else if(Integer.parseInt(txtBeras.getText())>zakatBeras){
            zakatBeras=Integer.parseInt(txtBeras.getText())-zakatBeras;
            zakatBeras=datfarMuzakki.totalBeras+zakatBeras;
        }else{
            zakatBeras=datfarMuzakki.totalBeras;
        }
        String sql="Update dt_muzakki set namaMuzakki='"+txtNamaMuzakki.getText().toUpperCase()+
                    "',alamat='"+txtAlamat.getText()+
                    "',jmlJiwa='"+Integer.toString(jiwa)+
                    "',jmlZakat='"+txtZakatFitrah.getText()+
                    "',jmlBeras='"+txtBeras.getText()+
                    "',panitia='"+koneksi.username+"' where nomor='"+datfarMuzakki.noEdit+"'";
        String updateLog="update log_muzakki set totalUang='"+zakatUang+"',totalBeras='"+zakatBeras+"'";
    try{
        try{
            k.st=k.conn.createStatement();
            k.st.execute(sql);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Penyimpanan Gagal");
            setKosongkan();
            txtNamaMuzakki.requestFocus();
            return;
        }
        try{
            k.st=k.conn.createStatement();
            k.st.execute(updateLog);
            //JOptionPane.showMessageDialog(this, "Data Berhasil Di Update");
        }catch(SQLException e){
            //JOptionPane.showMessageDialog(this, "Gagal Di Update");
            return;
        }
            JOptionPane.showMessageDialog(this, "Data Berhasil Di Edit");
            dispose();
    }catch(Exception e){
        JOptionPane.showMessageDialog(this, "Gagal Di Edit");
    }
    }//GEN-LAST:event_btnEditActionPerformed

