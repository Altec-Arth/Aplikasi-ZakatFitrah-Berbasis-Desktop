    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtNamaMuzakki.getText()==" "){
            JOptionPane.showMessageDialog(this, "Nama Muzakki Harus Di isi");
        }else if(txtAlamat.getText()==" "){
            JOptionPane.showMessageDialog(this, "Alamat Harus Di isi");
        }else if(modelJiwa.getNumber().intValue()==0){
            JOptionPane.showMessageDialog(this, "Jumlah Jiwa Minimal 1");
        }else if(txtZakatFitrah.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Besar Zakat Fitrah Harus di Isi");
        }else{
            int jmlBeras=0;
            if(txtBeras.getText().isEmpty()){
                jmlBeras=0;
            }else{
                jmlBeras=Integer.parseInt(txtBeras.getText());
            }
        int jiwa=modelJiwa.getNumber().intValue();
        total=jiwa*Integer.parseInt(txtZakatFitrah.getText());
        String sql="INSERT into dt_muzakki (nomor,tglInput,namaMuzakki,alamat,jmlJiwa,jmlZakat,jmlBeras,panitia) VALUE ("+
                       nomor+",'"+
                       txtTanggal.getText()+"','"+
                       txtNamaMuzakki.getText().toUpperCase()+"','"+
                       txtAlamat.getText()+"',"+
                       Integer.toString(jiwa)+","+
                       txtZakatFitrah.getText()+","+
                       Integer.toString(jmlBeras)+",'"+
                       koneksi.username+"')";
        //String updateLog="update log_muzakki set currDate='"+txtTanggal.getText()+"', total='"+txtZakatFitrah.getText()+"'";
        try{
            k.st=k.conn.createStatement();
            k.st.execute(sql);
            //k.st.close();
            //k.st=k.conn.createStatement();
            //k.st.executeUpdate(updateLog);
            //k.st.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "Penyimpanan Gagal");
            setKosongkan();
            txtNamaMuzakki.requestFocus();
            return;
        }
        JOptionPane.showMessageDialog(this,"Data Berhasil Disimpan");
        this.dispose();
        nomor=0;
        new inputMuzakki().setVisible(true);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

